void	ft_sort_four(t_stack **a, t_stack **b)
{
	ft_push_pb(a, b);
	ft_sort_three(a);
	ft_push_pa(a, b);
	if ((*a)->data > (*a)->next->data)
		ft_swap_a(a);
}
